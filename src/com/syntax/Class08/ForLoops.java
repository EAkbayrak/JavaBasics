package com.syntax.Class08;

public class ForLoops {

	public static void main(String[] args) {
		
		//for loop - executes block of code as long as condition is true
		//For loop is the best choice to use when know how many times we  want to repeat a block of code
		
		// for (initialization; condition; increment/decrement) {block of code}
		
		// I want to say GM 5 times
		
		
		for (int i=20; i>=5; i--) {
			String userName="Eakbayrak";
			
			System.out.println(userName);
		}
		
		
		
		
	}
;
}
