package com.syntax.Class07;

public class DoWhile {

	public static void main(String[] args) {

		int a=1;
		
		while (a<=3) {
			System.out.print(a+" ");
			a++;
		}
		
		System.out.println();
		System.out.println("------------------");
	

	int num1 = 1;
	
	// do while first executes the code
	// and only then checks the condition 
	int b=1;
	
		do {
			System.out.println(b);
			b++;
		}while (b<=30);
	
	int c=70;
	do {
		System.out.println(c);
		c-=2;
	} while (c>=30);
	
		
}}
